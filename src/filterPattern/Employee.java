package filterPattern;

public class Employee {

	private String name;
	private String job;
	private String gender;
	
	
	public Employee(String name, String job, String gender) {
		super();
		this.name = name;
		this.job = job;
		this.gender = gender;
	}
	public String getName() {
		return name;
	}
	public String getJob() {
		return job;
	}
	public String getGender() {
		return gender;
	}
		
	
}
